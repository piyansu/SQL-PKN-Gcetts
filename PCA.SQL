DROP TABLE EMPLOYEE ;
DROP TABLE DEPARTMENT ;

CREATE TABLE DEPARTMENT(
DEPT_NO NUMBER(3) PRIMARY KEY,
DEPT_NAME VARCHAR(20) NOT NULL ,
LOCATION VARCHAR(30) NOT NULL
);

CREATE TABLE EMPLOYEE(
EMP_NO NUMBER(4) ,
ENAME VARCHAR2(20) NOT NULL ,
SEX CHAR(1) NOT NULL,
JOB VARCHAR2(20) NOT NULL,
MGR_NO NUMBER(4),
DOJ DATE NOT NULL,
SAL NUMBER(8,2) NOT NULL,
COMM NUMBER(6,2) ,
DEPT_NO NUMBER(3) ,
CONSTRAINTS EMP_NO_PK PRIMARY KEY(EMP_NO) ,
CONSTRAINTS MGR_NO_FK FOREIGN KEY(MGR_NO) REFERENCES EMPLOYEE(EMP_NO) ,
CONSTRAINTS SEX_CK123 CHECK (SEX IN('M','F')),
CONSTRAINTS DEPT_NO_FK FOREIGN KEY(DEPT_NO) REFERENCES DEPARTMENT(DEPT_NO) 
);

DESC EMPLOYEE;
DESC DEPARTMENT ;

