--> STARTING SERVER 
SET SERVEROUTPUT ON ; 

DECLARE
-- ALL THE VARIABLES DECLARATION 
VAR NUMBER(3) := 1;  
N NUMBER (2) := 10 ; 
BEGIN
-- USE ALL VARIABLES
LOOP
    DBMS_OUTPUT.PUT_LINE(VAR); 
    VAR := VAR+1 ; 
    EXIT WHEN VAR>10 ;
END LOOP ; 

WHILE VAR<10
LOOP
    DBMS_OUTPUT.PUT_LINE(VAR); 
    VAR := VAR+1 ;
END LOOP ; 

FOR V IN 1..N
LOOP
    DBMS_OUTPUT.PUT_LINE(VAR); 
END LOOP ; 

END;
