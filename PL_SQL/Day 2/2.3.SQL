-- IMPLEMENT A CHECKING FOR DUPLICATE PRIME NUMBER ENTRY INTO THE TABLE PRIME ENTRY 

CREATE OR REPLACE FUNCTION FUN2 (A NUMBER) RETURN NUMBER
IS 

V PRIME_ENTRY.PID % TYPE ; 

BEGIN

SELECT PID INTO V FROM PRIME_ENTRY WHERE PNUM = A ; 
RETURN 1 ; 

EXCEPTION

WHEN NO_DATA_FOUND THEN
RETURN 0 ;

END;
/

DECLARE

X NUMBER(1) ; 

BEGIN

X := FUN2(5); 
DBMS_OUTPUT.PUT_LINE(X);

END; 
/