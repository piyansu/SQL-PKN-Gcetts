-- FOR WRITING EXPLIT CURSOR 
-- STEP 1 : DECLARE CURSOR 
-- STEP 2 : OPEN 
-- STEP 3 : FETCH 
-- STEP 4 : CLOSE

-- CURSOR FOR LOOP

DECLARE

    CURSOR PIYANSU IS SELECT ACNO , ACNAME FROM ACCOUNT_23 ; 

BEGIN
       
    FOR CRV IN PIYANSU
    LOOP 
        DBMS_OUTPUT.PUT_LINE(CRV.ACNO || CRV.ACNAME);
    END LOOP ; 

END;
/
